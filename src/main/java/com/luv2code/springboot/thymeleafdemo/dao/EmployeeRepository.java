public interface EmployeeRepository extends JpaRepository<Employee, Integer> {

	// that's it ... no need to write any code LOL!
	
	// add a method to sort by last name. Spring Data JPA will extract the method name and create corresponding query for us
	public List<Employee> findAllByOrderByLastNameAsc(); // => from Employee order by lastName asc

}
